/**
 * Regenerates email-macros-data.js from macros.json (the single source of truth).
 * Run: node scripts/build-email-macros.js
 *
 * macros.json is the ONLY file you edit by hand. It is loaded live by the macro
 * popout (see content.js), so new/edited macros show up in the sidebar as soon
 * as you reload the extension — no build step needed for that.
 *
 * This build step only exists to keep the (optional) Settings → Email templates
 * feature and the sidepanel email box in sync with macros.json. Run it after
 * editing macros.json if you use those features.
 */
const fs = require("fs");
const path = require("path");

const rootDir = path.join(__dirname, "..");
const macrosPath = path.join(rootDir, "macros.json");

const data = JSON.parse(fs.readFileSync(macrosPath, "utf8"));
const signature = data.signature || "Kind regards,\nEveryDayMeds Clinical Team";
const version = Number.isFinite(data.version) ? data.version : 1;
const categories = Array.isArray(data.categories) ? data.categories : [];

// Normalise: keep only the fields the extension consumes, in a stable order.
const cleanCategories = categories.map((c) => ({
  id: c.id,
  label: c.label,
  icon: c.icon,
  macros: (c.macros || [])
    .filter((m) => m && m.name && m.body)
    .map((m) => ({
      num: Number.isFinite(m.num) ? m.num : null,
      name: String(m.name),
      tag: m.tag ? String(m.tag) : "",
      desc: m.desc ? String(m.desc) : "",
      body: String(m.body),
    })),
}));

const flat = cleanCategories.flatMap((c) =>
  c.macros.map((m) => ({ num: m.num, name: m.name, text: m.body }))
);

const out = `/* AUTO-GENERATED by scripts/build-email-macros.js from macros.json — do not edit by hand */
const EDMS_MACRO_SIGNATURE = ${JSON.stringify(signature)};
const EMAIL_MACROS_VERSION = ${version};

const EDMS_MACRO_CATEGORIES = ${JSON.stringify(cleanCategories, null, 2)};

const EDMS_FLAT_MACROS = ${JSON.stringify(flat, null, 2)};

if (typeof window !== "undefined") {
  window.EDMS_MACRO_SIGNATURE = EDMS_MACRO_SIGNATURE;
  window.EMAIL_MACROS_VERSION = EMAIL_MACROS_VERSION;
  window.EDMS_MACRO_CATEGORIES = EDMS_MACRO_CATEGORIES;
  window.EDMS_FLAT_MACROS = EDMS_FLAT_MACROS;
}
`;

const outPath = path.join(rootDir, "email-macros-data.js");
fs.writeFileSync(outPath, out, "utf8");
console.log("Wrote", outPath, "-", flat.length, "macros in", cleanCategories.length, "categories");
